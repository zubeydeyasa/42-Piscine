#include <unistd.h>

void ft_putchar(char c)
{
    write(1, &c, 1);
}

void ft_print_numbers(void)
{
    char k;
    
    k = '0';
    while(k <= '9')
    {
        ft_putchar(k);
        k++;
    }
}